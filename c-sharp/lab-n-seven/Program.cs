using System;

class Program
{
    static void Main()
    {
        Console.WriteLine("Пример работы с указателями (unsafe)");

        // Блок unsafe позволяет работать с указателями, что используется редко в безопасном C#,
        // но помогает понять, как устроена память и как можно ссылаться на адреса напрямую.
        unsafe
        {
            // Простой указатель на переменную
            int a = 10;         // обычная целочисленная переменная
            int b = 25;         // ещё одна переменная
            int* p = &a;        // p хранит адрес переменной a
            Console.WriteLine($"a = {a}; через *p = {*p}");

            // Через оператор разыменования * можно получить значение по адресу.
            *p = 30;            // меняем значение a через указатель p
            Console.WriteLine($"После *p = 30 -> a = {a}");

            // Перенаправляем указатель на другую переменную.
            // Теперь p указывает не на a, а на b.
            p = &b;
            Console.WriteLine($"b = {b}; через *p = {*p}");

            // Указатель на указатель
            int* q = &a;            // q указывает на a
            int** pp = &q;          // pp указывает на переменную q, то есть на адрес указателя
            Console.WriteLine($"Через **pp: {**pp}");

            // Разыменовывая pp дважды, мы попадаем к значению a.
            **pp = **pp + 5;            // увеличиваем a на 5 через указатель на указатель
            Console.WriteLine($"После **pp += 5 -> a = {a}");

            // Получение адресов переменных для вывода
            // Преобразуем адреса в целочисленный тип ulong только для отображения.
            ulong addrA = (ulong)(&a);
            ulong addrB = (ulong)(&b);
            Console.WriteLine($"Адрес a: {addrA}");
            Console.WriteLine($"Адрес b: {addrB}");

            // Работа с массивом и fixed
            int[] arr = { 100, 200, 300, 400 };

            // fixed блок "закрепляет" массив в памяти, чтобы указатель на его элементы не стал недействительным
            // во время работы сборщика мусора.
            fixed (int* pa = arr)
            {
                Console.WriteLine($"arr[0] через *pa = {*pa}");

                // Арифметика указателей: pa + 2 указывает на третий элемент массива.
                int* p2 = pa + 2;     // указатель на arr[2]
                Console.WriteLine($"arr[2] через *(pa+2) = {*p2}");

                // Меняем второй элемент массива через указатель.
                *(pa + 1) = 250;
                Console.WriteLine($"После изменения arr[1] = {arr[1]}");
                Console.WriteLine($"Адрес arr (pa): {(ulong)pa}");
            }

            // stackalloc: выделение массива на стеке
            // stackalloc создаёт временную область памяти в стеке, что быстрее,
            // но подходит только для небольших массивов и короткого времени жизни.
            Span<int> span = stackalloc int[3] { 7, 8, 9 };

            // Для Span тоже можно получить указатель через fixed.
            fixed (int* ps = span)
            {
                Console.WriteLine($"stackalloc[0] = {*ps}");
                *(ps + 2) = 99;
                Console.WriteLine($"stackalloc после: {span[2]}");
            }
        }

        Console.WriteLine("Готово.");
    }
}
